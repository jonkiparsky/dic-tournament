package tictactoe;

/**
 * 
 * Represents a grid marker.
 * 
 * @author Ryan Beckett
 */
public enum GridMark
{
    /**
     * Represents the 1st player grid marker.
     */
    X,
    /**
     * Represents the 2nd player grid marker.
     */
    O,
    /**
     * Represents an empty space on the grid.
     */
    E;
}
